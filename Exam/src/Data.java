
public abstract class Data {

    protected double triangleheight;
    protected double triangledownline;
    protected double squareline;
    protected double parheight;
    protected double parweight;

    public Data(double triangleheight, double triangledownline, double squareline, double parheight, double parweight) {
        this.triangleheight = triangleheight;
        this.triangledownline = triangledownline;
        this.squareline = squareline;
        this.parheight = parheight;
        this.parweight = parweight;
    }


}
