
public class Paral {
    protected double parheight;
    protected double parweight;

    public Paral(double parheight, double parweight) {
        this.parheight = parheight;
        this.parweight = parweight;
    }

    public double getParheight() {
        return parheight;
    }

    public double getParweight() {
        return parweight;
    }
}
